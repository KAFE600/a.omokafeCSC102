# A python program to solve a compound interest problem.

Principal = input("Insert the principal :")
Rate = input("Insert the rate :")
n = input("Insert the n :")
t = input("Insert the t :")
Final_Amount = int(Principal)* (1 + int(Rate)/int(n))**(int(n)*int(t)) 


print(Principal)
print(Rate)
print(n)
print(t)
print(Final_Amount)
















